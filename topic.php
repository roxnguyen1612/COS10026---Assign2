      <h2>WHAT IS MP3?</h2>
      <p><dfn title="MP3">MP3(MPEG-1 Audio Layer III)</dfn> is a coding format for digital audio. MP3 is also a means of
        compressing a sound sequence into a very small file, to enable digital storage and transmission.</p>
    </div>
    <dl>
      <dt><strong>Purpose:</strong></dt>
      <dd>- Compress audio files with minimal loss to the quality of sound, retaining close to CD quality. </dd>
      <dd>- Compress audio files by a factor of 10 or 12 by using data-compression algorithm. </dd>
      <dt><strong>Main features:</strong></dt>
      <dd>- Faster download, more free spaces.</dd>
      <dd>- MP3 format uses characteristics of how humans hear sounds to design the compression algorithm. For example:
        <ul>
          <li>Certain sounds the human ear cannot hear.</li>
          <li>Certain sounds that the human ear hears much better than others.</li>
          <li>Certain parts of a song can be eliminated without significantly.</li>
          <li>If there are two sounds playing simultaneously, we hear the louder one but struggle to hear the softer
            one.</li>
        </ul>
      </dd>
      <dd>- Shrink songs considerably by a factor of at least 10 without major loss to quality.</dd>
    </dl>
  </section>

